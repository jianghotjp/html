define(function(require, exports, module) {
	var $ = require('$');


	exports.getYedaDeptType = function(key) {
		var value = "";
		if (!key || key.length == 0) {
			return "";
		}
		if (key == "1001001") {
			value = "烟台农村商业银行";
		} else if (key == "1001002") {
			value = "烟台银行";

		} else if (key == "1001003") {
			value = "中国银行";
		}
		return value;
	}

});